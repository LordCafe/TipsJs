// método push: agrega un nuevo elemento al final del arreglo
const temp = []

temp.push(37.5)
temp.push(33.6)
temp.push(28.6)
temp.push(20.6)

// RESULTADO ----> [37.5, 33.6, 28.6, 20.6]

// método unshift: agrega un nuevo elemento al comienzo del arreglo
const temp2 = []

temp2.unshift(37.5)
temp2.unshift(33.6)
temp2.unshift(28.6)
temp2.unshift(20.6)

// RESULTADO ------> [20.6, 28.6, 33.6, 37.5]